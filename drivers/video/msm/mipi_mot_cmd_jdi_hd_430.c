int check_dsi_error(void)
{
	int ret = -1;
	u8 rdata = 0;

	ret = mipi_mot_rx_cmd(&chk_dsi_err_cmd, &rdata, 1);
	if (ret < 0) {
		pr_err("%s: failed to read DSI 05h,ret %d\n", __func__, ret);
		ret = -1;
	} else
		ret = rdata;

	return ret;
}

